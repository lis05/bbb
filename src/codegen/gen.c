static cb_t gen_extern_decl(int indent, const struct extern_declaration_node_t *node) {
    log_debug("Entered with indent=%d\n", indent);
    log_assert(node != NULL);

    cb_t res;
    cb_init(&res);

    if (strcmp(node->kw->name, "extern") == 0) {
        log_debug("Generating extern declaration\n");
        EXPLAIN(res, indent, "Extern daclaration.\n");

        char *line = (char*)malloc(strlen("extern ") + strlen(node->name->name) + 1 + 1);
        log_assert(line != NULL);

        strcpy(line, "extern ");
        strcat(line, node->name->name);
        log_debug(" - appending '%s'\n", line);
        strcat(line, "\n");
        cb_add_back(&res, indent, line);
        free(line);

        return res;
    }
    else if (strcmp(node->kw->name, "nasm") == 0) {
        // todo
        return res;
    }
    else {
        context_msg(&node->frag, "Unknown declaration (wrong keyword)");
        return cb_invalid();
    }
}
