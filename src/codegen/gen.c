#define EXPLAIN(cbref, indent, msg)                  \
    do {                                             \
        if (add_explanatory_comments) {              \
            cb_add_back(&(cbref), indent, "; " msg); \
        }                                            \
    } while (0)

static cb_t gen_extern_decl(int                                     indent,
                            const struct extern_declaration_node_t *node);
static cb_t gen_program(int indent, const struct program_node_t *node);

cb_t gen(struct program_node_t *prog) {
    cb_t res;
    cb_init(&res);

    EXPLAIN(res, 0, "NASM generated by bbb for this program begins here.\n");

    cb_t other = gen_program(0, prog);
    if (!cb_is_valid(&other)) {
        return other;
    }
    cb_glue_back(&res, &other);

    EXPLAIN(res, 0, "NASM generated by bbb for this program ends here.\n");
    return res;
}

